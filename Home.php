<h1 class="heading">Blood Group</h1>
<p class="paragraph">The human body contains around 8 to 10 pints of blood depending on the size of the individual. However, the composition of the blood is not the same in each person. This is what makes the person's blood type.</p><br>
<p class="heading left1">There are four ABO groups:</p>
<ol class="paragraph left1 space">
<li>Group A: The surface of the red blood cells contains A antigen, and the plasma has anti-B antibody that would attack any foreign B antigen containing red blood cells.</li>
<li>Group B: The surface of the red blood cells contains B antigen, and the plasma has anti-A antibody that would attack any foreign A antigen containing red blood cells.</li>
<li>Group AB: The red blood cells have both A and B antigens, but the plasma does not contain anti-A/anti-B antibodies. Individuals with type AB can receive any ABO blood type.</li>
<li>Group O: The plasma contains both types of anti-A/anti-B antibodies, but the surface of the red blood cells does not contain any A/B antigens. Having none of these A/B antigens means that they can be donated to a person with any ABO blood type.</li>
</ol>
<h1 class="heading left1 space1">Why Donate Blood?</h1>
<p class="paragraph">A blood donation truly is a gift of life that a healthy individual can give to others in their community who are sick or injured. In one hours time, a person can donate one unit of blood that can be separated into four individual components that could help save multiple lives.</p>
<p class="paragraph">From one unit of blood, red blood cells can be extracted for use in trauma or surgical patients. Plasma, the liquid part of blood, is administered to patients with clotting problems. The third component of blood, platelets, clot the blood when cuts or other open wounds occur, and are often used in cancer and transplant patients. Cryoprecipitated anti-hemophilic factor (AHF) is also used for clotting factors.</p>
